/**
 * Escape a string for safe insertion into HTML text or double-quoted attributes.
 * Shared helper — mirrors the private copies in the markdown tools.
 */
export function escapeHtml(str: string): string {
  return str
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}
